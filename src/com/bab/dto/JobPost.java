package com.bab.dto;

public class JobPost {
	
	private String jobId;
	private String jobTitle;
	private String jobDescription;
	private String jobCompany;
	private String jobLocation;
	private String jobExp;
	private String jobSkills;
	private String jobOwnerEmailId;
	private String createDate;
	private String updateDate;
	private String postDay;
	private String postMonthYear;
	
	
	
	
	public String getJobId() {
		return jobId;
	}
	public void setJobId(String jobId) {
		this.jobId = jobId;
	}
	public String getJobTitle() {
		return jobTitle;
	}
	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}
	public String getJobDescription() {
		return jobDescription;
	}
	public void setJobDescription(String jobDescription) {
		this.jobDescription = jobDescription;
	}
	public String getJobCompany() {
		return jobCompany;
	}
	public void setJobCompany(String jobCompany) {
		this.jobCompany = jobCompany;
	}
	public String getJobLocation() {
		return jobLocation;
	}
	public void setJobLocation(String jobLocation) {
		this.jobLocation = jobLocation;
	}
	public String getJobExp() {
		return jobExp;
	}
	public void setJobExp(String jobExp) {
		this.jobExp = jobExp;
	}
	public String getJobSkills() {
		return jobSkills;
	}
	public void setJobSkills(String jobSkills) {
		this.jobSkills = jobSkills;
	}
	public String getJobOwnerEmailId() {
		return jobOwnerEmailId;
	}
	public void setJobOwnerEmailId(String jobOwnerEmailId) {
		this.jobOwnerEmailId = jobOwnerEmailId;
	}
	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public String getUpdateDate() {
		return updateDate;
	}
	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}
	public String getPostDay() {
		return postDay;
	}
	public void setPostDay(String postDay) {
		this.postDay = postDay;
	}
	public String getPostMonthYear() {
		return postMonthYear;
	}
	public void setPostMonthYear(String postMonthYear) {
		this.postMonthYear = postMonthYear;
	}


	
		
	
}
