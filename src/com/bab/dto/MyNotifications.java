package com.bab.dto;

public class MyNotifications {
	private String jobTitle;
	private String jobAppliedDate;
	private String jobApplierName;
	private String jobApplierEmail;
	private String jobApplierContact;
	public String getJobAppliedDate() {
		return jobAppliedDate;
	}
	public void setJobAppliedDate(String jobAppliedDate) {
		this.jobAppliedDate = jobAppliedDate;
	}
	public String getJobApplierName() {
		return jobApplierName;
	}
	public void setJobApplierName(String jobApplierName) {
		this.jobApplierName = jobApplierName;
	}
	public String getJobApplierEmail() {
		return jobApplierEmail;
	}
	public void setJobApplierEmail(String jobApplierEmail) {
		this.jobApplierEmail = jobApplierEmail;
	}
	public String getJobApplierContact() {
		return jobApplierContact;
	}
	public void setJobApplierContact(String jobApplierContact) {
		this.jobApplierContact = jobApplierContact;
	}
	public String getJobTitle() {
		return jobTitle;
	}
	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}
	
	

}
